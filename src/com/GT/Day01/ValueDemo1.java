package com.GT.Day01;

public class ValueDemo1 {
    public static void main(String[] args) {
        //整数类型
        System.out.println(246);
        //小数类型
        System.out.println(22.7);
        //字符类型
        System.out.println('男');
        //布尔类型
        System.out.println(true);
        //空类型
        System.out.println("null");
        //字符串类型
        System.out.println("老妈最漂亮！");
        //\t制表符打印
        System.out.println("cname"+'\t'+99);
        System.out.println("cno"+'\t'+2);
    }
}
